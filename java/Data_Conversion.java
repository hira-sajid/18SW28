class Data_Conversion
{
	public static void main(String [] args)
	{
		double d=50.25;
		long l=(long)50.25;
		System.out.println("This is value of double ="+d);
	System.out.println("This is conversion of double into long ="+l);
	System.out.println("This is fractional part of double ="+(d-l));
	}
}